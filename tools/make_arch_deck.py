from pptx import Presentation
from pptx.util import Inches, Pt
from pptx.dml.color import RGBColor
from pptx.enum.text import PP_ALIGN

INK = RGBColor(0x2B, 0x23, 0x50)
ACCENT = RGBColor(0x5B, 0x3F, 0xFF)
BG_DARK = RGBColor(0x1E, 0x17, 0x3A)
WHITE = RGBColor(0xFF, 0xFF, 0xFF)
GLOW = RGBColor(0xFF, 0xD1, 0x66)
GREY = RGBColor(0x6B, 0x64, 0x8B)

prs = Presentation()
prs.slide_width = Inches(13.333)
prs.slide_height = Inches(7.5)
BLANK = prs.slide_layouts[6]


def bgfill(slide, color):
    slide.background.fill.solid()
    slide.background.fill.fore_color.rgb = color


def textbox(slide, l, t, w, h):
    box = slide.shapes.add_textbox(Inches(l), Inches(t), Inches(w), Inches(h))
    tf = box.text_frame
    tf.word_wrap = True
    return tf


def notes(slide, text):
    slide.notes_slide.notes_text_frame.text = text


def title_slide(title, subtitle, note):
    s = prs.slides.add_slide(BLANK)
    bgfill(s, BG_DARK)
    tf = textbox(s, 1.0, 2.4, 11.3, 1.6)
    p = tf.paragraphs[0]
    r = p.add_run(); r.text = title
    r.font.size = Pt(44); r.font.bold = True; r.font.color.rgb = WHITE
    tf2 = textbox(s, 1.0, 4.1, 11.3, 1.8)
    for line in subtitle.split("\n"):
        p = tf2.add_paragraph(); p.text = line
        p.font.size = Pt(18); p.font.color.rgb = GLOW
    notes(s, note)
    return s


def section_slide(kicker, title, note):
    s = prs.slides.add_slide(BLANK)
    bgfill(s, BG_DARK)
    tf = textbox(s, 1.0, 2.6, 11.3, 0.7)
    p = tf.paragraphs[0]; r = p.add_run(); r.text = kicker
    r.font.size = Pt(18); r.font.color.rgb = GLOW
    tf2 = textbox(s, 1.0, 3.3, 11.3, 1.6)
    p = tf2.paragraphs[0]; r = p.add_run(); r.text = title
    r.font.size = Pt(36); r.font.bold = True; r.font.color.rgb = WHITE
    notes(s, note)
    return s


def content_slide(title, bullets, note, kicker=None):
    """bullets: list of (level, text, emphasize)"""
    s = prs.slides.add_slide(BLANK)
    bgfill(s, WHITE)
    if kicker:
        tf0 = textbox(s, 0.7, 0.32, 11.9, 0.4)
        p = tf0.paragraphs[0]; r = p.add_run(); r.text = kicker
        r.font.size = Pt(13); r.font.color.rgb = GREY
    tf = textbox(s, 0.7, 0.68, 11.9, 0.9)
    p = tf.paragraphs[0]; r = p.add_run(); r.text = title
    r.font.size = Pt(28); r.font.bold = True; r.font.color.rgb = INK
    body = textbox(s, 0.7, 1.65, 11.9, 5.5)
    first = True
    for level, text, em in bullets:
        p = body.paragraphs[0] if first else body.add_paragraph()
        first = False
        p.level = level
        r = p.add_run(); r.text = ("• " if level == 0 else "– ") + text
        r.font.size = Pt(17 if level == 0 else 14.5)
        r.font.bold = bool(em)
        r.font.color.rgb = ACCENT if em else INK
        p.space_after = Pt(7 if level == 0 else 3)
    notes(s, note)
    return s


